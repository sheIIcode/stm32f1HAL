void calibrate_sensors() {
  int                   num_readings = 10;
  float                 x_accel = 0;
  float                 y_accel = 0;
  float                 z_accel = 0;
  float                 x_gyro = 0;
  float                 y_gyro = 0;
  float                 z_gyro = 0;

  //Serial.println("Starting Calibration");

  // Discard the first set of values read from the IMU
  SD_MPU6050_ReadGyroscope(&hi2c1,&mpu1);
  SD_MPU6050_ReadAccelerometer(&hi2c1,&mpu1);

  // Read and average the raw values from the IMU
  for (int i = 0; i < num_readings; i++) {
	  SD_MPU6050_ReadGyroscope(&hi2c1,&mpu1);
	  SD_MPU6050_ReadAccelerometer(&hi2c1,&mpu1);

    x_accel += mpu1.Accelerometer_X;
    y_accel += mpu1.Accelerometer_Y;
    z_accel += mpu1.Accelerometer_Z;
    x_gyro += mpu1.Gyroscope_X;
    y_gyro += mpu1.Gyroscope_Y;
    z_gyro += mpu1.Gyroscope_Z;
    HAL_Delay(100);
  }
  x_accel /= num_readings;
  y_accel /= num_readings;
  z_accel /= num_readings;
  x_gyro /= num_readings;
  y_gyro /= num_readings;
  z_gyro /= num_readings;

  // Store the raw calibration values globally
  base_x_accel = x_accel;
  base_y_accel = y_accel;
  base_z_accel = z_accel;
  base_x_gyro = x_gyro;
  base_y_gyro = y_gyro;
  base_z_gyro = z_gyro;
}

